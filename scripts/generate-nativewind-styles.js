#!/usr/bin/env node
/**
 * Pre-generates NativeWind/Tailwind styles for Expo SDK 56 compatibility.
 *
 * NativeWind v4 + react-native-css-interop v0.2.x is incompatible with
 * Metro 0.84.4 (Expo SDK 56) because the transformer replacement breaks
 * Metro's Bundler initialization. This script pre-compiles Tailwind CSS
 * and outputs a JS file that can be imported at app startup to inject styles.
 *
 * Usage: node scripts/generate-nativewind-styles.js
 */

const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const INPUT = path.join(ROOT, "global.css");
const OUTPUT_CSS = path.join(ROOT, ".cache", "tailwind-native.css");
const OUTPUT_JS = path.join(ROOT, "src", "styles", "nativewind-styles.generated.js");

// Ensure output directories exist
fs.mkdirSync(path.dirname(OUTPUT_CSS), { recursive: true });
fs.mkdirSync(path.dirname(OUTPUT_JS), { recursive: true });

console.log("🎨 Generating NativeWind styles...");

// Step 1: Run Tailwind CLI to generate CSS
try {
  execSync(
    `npx tailwindcss -i "${INPUT}" -o "${OUTPUT_CSS}" --config tailwind.config.js`,
    { cwd: ROOT, stdio: "pipe" }
  );
  console.log("✅ Tailwind CSS generated");
} catch (err) {
  console.error("❌ Tailwind CLI failed:", err.message);
  // Write empty styles as fallback
  fs.writeFileSync(
    OUTPUT_JS,
    `// Auto-generated by scripts/generate-nativewind-styles.js\nexport const nativewindStyles = {};\n`
  );
  process.exit(0);
}

// Step 2: Convert CSS to React Native runtime format
let cssData;
try {
  cssData = fs.readFileSync(OUTPUT_CSS, "utf8");
} catch (err) {
  console.error("❌ Could not read CSS output:", err.message);
  process.exit(1);
}

let runtimeData = {};
try {
  const { cssToReactNativeRuntime } = require("react-native-css-interop/dist/css-to-rn");
  runtimeData = cssToReactNativeRuntime(Buffer.from(cssData), {}, () => {});
  console.log("✅ CSS converted to React Native runtime format");
} catch (err) {
  console.warn("⚠️  css-to-rn conversion failed, using empty styles:", err.message);
  runtimeData = {};
}

// Step 3: Write JS output
const jsContent = `// Auto-generated by scripts/generate-nativewind-styles.js
// Do NOT edit manually. Run: node scripts/generate-nativewind-styles.js
// Generated: ${new Date().toISOString()}

export const nativewindStyles = ${JSON.stringify(runtimeData, null, 2)};
`;

fs.writeFileSync(OUTPUT_JS, jsContent);
console.log(`✅ Styles written to ${path.relative(ROOT, OUTPUT_JS)}`);
console.log("🎉 NativeWind styles generated successfully!");
